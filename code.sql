-- 1. Student Report Card (Calculates Grade on the fly instead of a trigger)
SELECT 
    s.full_name AS student_name, 
    c.course_name, 
    sem.semester_name, 
    e.marks,
    CASE 
        WHEN e.marks >= 90 THEN 'A'
        WHEN e.marks >= 75 THEN 'B'
        WHEN e.marks >= 60 THEN 'C'
        WHEN e.marks >= 50 THEN 'D'
        ELSE 'F'
    END AS calculated_grade
FROM enrollments e
JOIN students s    ON e.student_id = s.student_id
JOIN courses c     ON e.course_id = c.course_id
JOIN semesters sem ON e.semester_id = sem.semester_id;

-- 2. Outstanding Fee Balances (Calculates Status on the fly instead of a trigger)
SELECT 
    s.full_name AS student_name, 
    sem.semester_name, 
    f.amount_due, 
    f.amount_paid,
    (f.amount_due - f.amount_paid) AS balance_remaining,
    CASE
        WHEN f.amount_paid >= f.amount_due THEN 'Paid'
        WHEN f.amount_paid > 0 THEN 'Partial'
        ELSE 'Unpaid'
    END AS fee_status
FROM fees f
JOIN students s    ON f.student_id = s.student_id
JOIN semesters sem ON f.semester_id = sem.semester_id
WHERE (f.amount_due - f.amount_paid) > 0;

-- 3. Department Summary Dashboard (Counts totals)
SELECT 
    d.dept_name, 
    COUNT(DISTINCT s.student_id) AS total_students,
    COUNT(DISTINCT c.course_id)  AS total_courses,
    ROUND(AVG(e.marks), 2)       AS avg_marks
FROM departments d
LEFT JOIN students s    ON s.dept_id = d.dept_id
LEFT JOIN courses c     ON c.dept_id = d.dept_id
LEFT JOIN enrollments e ON e.student_id = s.student_id
GROUP BY d.dept_name;

-- 4. Course load per instructor
SELECT 
    i.full_name AS instructor_name, 
    COUNT(c.course_id) AS courses_taught
FROM instructors i
JOIN courses c ON c.instructor_id = i.instructor_id
GROUP BY i.full_name;