-- =====================================================================
-- STEP 1: CLEANUP (Ignore errors on the very first run)
-- =====================================================================
DROP TABLE fees CASCADE CONSTRAINTS;
DROP TABLE attendance CASCADE CONSTRAINTS;
DROP TABLE enrollments CASCADE CONSTRAINTS;
DROP TABLE courses CASCADE CONSTRAINTS;
DROP TABLE semesters CASCADE CONSTRAINTS;
DROP TABLE instructors CASCADE CONSTRAINTS;
DROP TABLE students CASCADE CONSTRAINTS;
DROP TABLE departments CASCADE CONSTRAINTS;

-- =====================================================================
-- STEP 2: CREATE TABLES (Core Structure)
-- =====================================================================
CREATE TABLE departments (
    dept_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dept_name   VARCHAR2(100) NOT NULL UNIQUE,
    dept_head   VARCHAR2(100)
);

CREATE TABLE instructors (
    instructor_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name     VARCHAR2(100) NOT NULL,
    dept_id       NUMBER NOT NULL REFERENCES departments(dept_id),
    email         VARCHAR2(150) UNIQUE NOT NULL,
    salary        NUMBER(10,2) CHECK (salary > 0)
);

CREATE TABLE students (
    student_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name       VARCHAR2(100) NOT NULL,
    dept_id         NUMBER NOT NULL REFERENCES departments(dept_id),
    dob             DATE NOT NULL,
    email           VARCHAR2(150) UNIQUE NOT NULL,
    enrollment_year NUMBER(4) NOT NULL
);

CREATE TABLE semesters (
    semester_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    semester_name VARCHAR2(50) NOT NULL UNIQUE,
    start_date    DATE NOT NULL,
    end_date      DATE NOT NULL
);

CREATE TABLE courses (
    course_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_code   VARCHAR2(20) NOT NULL UNIQUE,
    course_name   VARCHAR2(100) NOT NULL,
    dept_id       NUMBER NOT NULL REFERENCES departments(dept_id),
    credits       NUMBER(1) NOT NULL CHECK (credits BETWEEN 1 AND 6),
    instructor_id NUMBER REFERENCES instructors(instructor_id)
);

CREATE TABLE enrollments (
    enrollment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id    NUMBER NOT NULL REFERENCES students(student_id),
    course_id     NUMBER NOT NULL REFERENCES courses(course_id),
    semester_id   NUMBER NOT NULL REFERENCES semesters(semester_id),
    marks         NUMBER(5,2) CHECK (marks BETWEEN 0 AND 100),
    UNIQUE (student_id, course_id, semester_id)
);

CREATE TABLE attendance (
    attendance_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id    NUMBER NOT NULL REFERENCES students(student_id),
    course_id     NUMBER NOT NULL REFERENCES courses(course_id),
    class_date    DATE NOT NULL,
    status        VARCHAR2(10) NOT NULL CHECK (status IN ('Present','Absent','Late'))
);

CREATE TABLE fees (
    fee_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id   NUMBER NOT NULL REFERENCES students(student_id),
    semester_id  NUMBER NOT NULL REFERENCES semesters(semester_id),
    amount_due   NUMBER(10,2) NOT NULL,
    amount_paid  NUMBER(10,2) DEFAULT 0 NOT NULL
);

-- =====================================================================
-- STEP 3: INDEXES (Speeds up database searches)
-- =====================================================================
CREATE INDEX idx_students_dept       ON students(dept_id);
CREATE INDEX idx_courses_dept        ON courses(dept_id);
CREATE INDEX idx_enrollments_student ON enrollments(student_id);
CREATE INDEX idx_enrollments_course  ON enrollments(course_id);
CREATE INDEX idx_attendance_student  ON attendance(student_id);
CREATE INDEX idx_fees_student        ON fees(student_id);

COMMIT;

